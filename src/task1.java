public class task1 {
    public static void main(String[] args) {
        int a = 100;
        int b = 5;
        if (a > b) {
            System.out.println("a>b");
        } else if (a < b) {
            System.out.println("a<b");
        } else if (a == b) {
            System.out.println("a=b");
        }
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a / b);
        System.out.println(a * b);
    }
}
